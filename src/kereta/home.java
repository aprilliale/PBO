    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if("".equals(nama.getText()) || "".equals(kategori.getText()) || "".equals(harga.getText())
            || "".equals(jumlah.getText()) || "".equals(kode.getSelectedItem()) || "".equals(asal.getSelectedItem()) || 
                "".equals(tujuan.getSelectedItem()))
        {
            JOptionPane.showMessageDialog(this, "Harap lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else {
            
            String SQL = "INSERT INTO data_tiket (kode_kereta,nama,stasiun_asal,stasiun_tujuan,ketrgori,jumlah_tiket,harga) "
                    + "VALUES ('"+kode.getSelectedItem()+"', '"+nama.getText()+"', '"+asal.getSelectedItem()+"', '"+
                    tujuan.getSelectedItem()+"', '"+kategori.getText()+"', '"+jumlah.getText()+"', '"+harga.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }
            else{
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

