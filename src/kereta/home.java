private void selectData() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String kolom[]= {"kode_kereta","nama","stasiun_asal","stasiun_tujuan","ketrgori","jumlah_tiket","harga"};
         DefaultTableModel dtm = new DefaultTableModel(null, kolom);
         String SQL = "SELECT * FROM data_tiket";
         ResultSet rs = KoneksiDB.executeQuery(SQL);
         try{
             while(rs.next()){
                 String kode = rs.getString(1);
                 String nama = rs.getString(2);
                 String asal = rs.getString(3);
                 String tujuan =rs.getString(4);
                 String kategori = rs.getString(5);
                 String jumlah = rs.getString(6);
                 String harga = rs.getString(7);
                 String data[]= {kode,nama,asal,tujuan,kategori,jumlah,harga};
                 dtm.addRow(data);
             }
         }catch(SQLException ex){
             Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
         }
         tData.setModel(dtm);
    }
